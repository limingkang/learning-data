		i = 1,
		length = arguments.length,
		deep = false;   //默认是浅复制

	if ( typeof target === "boolean" ) {
		deep = target;

		// 确定第二个参数，即要返回的结果
		target = arguments[ i ] || {};
		i++;
	}

	// 如果第二个参数不是对象或者函数那么就要新建一个对象准备复制好以后返回
	if ( typeof target !== "object" && !jQuery.isFunction(target) ) {
		target = {};
	}

	// 当只有一个参数的时候，jQuery.extend调用时候this是指向jQuery的对象，并把整个jQuery对象赋值给target
	//jQuery.fn.extend调用时候this指向的是jQuery的原型对象，所以实例化后可访问
	if ( i === length ) {
		target = this;
		i--;
	}
    //向target里面添加我写的对象，并返回整个对象，也就达到了扩展作用
