//各种异步对象的构建以及在文档方面的应用
var rnotwhite = (/\S+/g);  //匹配任何非空白字符(全局匹配)
var optionsCache = {};     //参数缓存格式
// 将字符串格式的参数，改为我们所需要的缓存对象格式
//match方法返回匹配的数组
function createOptions( options ) {
	var object = optionsCache[ options ] = {};
	jQuery.each( options.match( rnotwhite ) || [], function( _, flag ) {
		object[ flag ] = true;
	});
	return object;
}
// once:	确保这个回调列表只执行.fire()一次，之后只不再执行.fire()
// memory:	保持以前的值,将上一次fire的参数保存到缓存当中，之后每add一次，就会触发fire()一次，注意这次只是运行你添加的那个函数，而且参数为上一次fire的参数
// unique:	确保一次只能添加一个回调(所以在列表中没有重复的回调)
// stopOnFalse:当一个回调返回false 时中断调用
//这个回调队列方法会应用到deferred中去
jQuery.Callbacks = function( options ) {

	//通过字符串在optionsCache寻找有没有相应缓存，如果没有则创建一个，有则引用
    //如果是对象则通过jQuery.extend深复制后赋给options。
	options = typeof options === "string" ?
		( optionsCache[ options ] || createOptions( options ) ) :
		jQuery.extend( {}, options );

	var
		memory,		 // 最后一次触发回调时传的参数
		fired,		 // 列表中的函数是否已经至少回调一次
		firing,		 // 列表中的函数是否正在回调中
		firingStart, // 回调起点
		firingLength,// 回调时的循环结尾
		firingIndex, // 当前正在回调的函数索引
		list = [],	 // 回调函数列表
		stack = !options.once && [],  //可重复的回调函数堆栈，用于控制触发回调时的参数列表

		fire = function( data ) {
			memory = options.memory && data;   //如果memory参数为true,则记录data
			fired = true;     //标记触发回调
			firingIndex = firingStart || 0;
			firingStart = 0;
			firingLength = list.length;
			firing = true;    //标记正在触发回调
			for ( ; list && firingIndex < firingLength; firingIndex++ ) {
				if ( list[ firingIndex ].apply( data[ 0 ], data[ 1 ] ) === false && options.stopOnFalse ) {
					memory = false; // 阻止未来可能由于add所产生的回调
					break;          //由于参数stopOnFalse为true，所以当有回调函数返回值为false时退出循环
				}
			}
			//标记回调结束
			firing = false;
			if ( list ) {
				if ( stack ) {
					if ( stack.length ) {
						fire( stack.shift() );
					}
				} else if ( memory ) {
					list = [];
				} else {
					self.disable();
				}
			}
		},
		// 暴露在外的Callbacks对象,对外接口
		self = {
			// 回调列表中添加一个回调或回调的集合
			add: function() {
				if ( list ) {
					// 首先存储当前列表的长度
					var start = list.length;
					(function add( args ) {
						jQuery.each( args, function( _, arg ) {
							var type = jQuery.type( arg );
							if ( type === "function" ) {
								if ( !options.unique || !self.has( arg ) ) {
									list.push( arg );
								}
							//如果是类数组或者对象，则递归
							} else if ( arg && arg.length && type !== "string" ) {
								add( arg );
							}
						});
					})( arguments );

					// 如果在执行Callbacks.add操作的状态为firing时，那么需要更新firingLength值
					if ( firing ) {
						firingLength = list.length;
					// With memory, if we're not firing then
					// we should call right away
					} else if ( memory ) {
						//如果options.memory为true，则将memory做为参数，应用最近增加的回调函数
						firingStart = start;
						fire( memory );
					}
				}
				return this;
			},
			// 在列表中删除函数(集)
			remove: function() {
				if ( list ) {
					jQuery.each( arguments, function( _, arg ) {
						var index;
						// while循环的意义在于借助jQuery.inArray删除函数列表中相同的函数引用（没有设置unique的情况）
                        // jQuery.inArray将每次返回查找到的元素的index作为自己的第三个参数继续进行查找，直到函数列表的尽头
                        // splice删除数组元素，修改数组的结构
						while ( ( index = jQuery.inArray( arg, list, index ) ) > -1 ) {
							list.splice( index, 1 );
							// 在函数列表处于firing状态时，最主要的就是维护firingLength和firgingIndex这两个值
                            // 保证fire时函数列表中的函数能够被正确执行（fire中的for循环需要这两个值
							if ( firing ) {
								if ( index <= firingLength ) {
									firingLength--;
								}
								if ( index <= firingIndex ) {
									firingIndex--;
								}
							}
						}
					});
				}
				return this;
			},
			// 回调函数是否在列表中
			has: function( fn ) {
				return fn ? jQuery.inArray( fn, list ) > -1 : !!( list && list.length );
			},
			// 从列表中删除所有回调函数
			empty: function() {
				list = [];
				firingLength = 0;
				return this;
			},
			// 禁用回调列表中的回调
			disable: function() {
				list = stack = memory = undefined;
				return this;
			},
			// 列表中是否被禁用
			disabled: function() {
				return !list;
			},
			// 锁定列表当前状态
			lock: function() {
				stack = undefined;
				if ( !memory ) {
					self.disable();
				}
				return this;
			},
			// 列表是否被锁
			locked: function() {
				return !stack;
			},
			// 以给定的上下文和参数调用所有回调函数
			fireWith: function( context, args ) {
				if ( list && ( !fired || stack ) ) {
					args = args || [];
					args = [ context, args.slice ? args.slice() : args ];
					if ( firing ) {
						stack.push( args );
					} else {
						fire( args );
					}
				}
				return this;
			},
			// 以给定的参数调用所有回调函数
			fire: function() {
				self.fireWith( this, arguments );
				return this;
			},
			// 回调函数离了表是否至少被调用一次
			fired: function() {
				return !!fired;
			}
		};

	return self;
};
//对原型方法deferred和promise的定义扩展,就用到了上面的Callbacks方法
jQuery.extend({
    // jQuery. Deferred主要处理：
	// 显而易见Deferred是个工厂类，返回的是内部构建的deferred对象
	// tuples 创建三个$.Callbacks对象，分别表示成功，失败，处理中三种状态
	// 创建了一个promise对象，具有state、always、then、promise方法
	// 扩展primise对象生成最终的Deferred对象，返回该对象
	// 通过promise.promise( deferred );混入到deferred中使用不在拥有resolve(With), reject(With), notify(With)这些能改变deferred对象状态并且执行callbacklist的方法了，是只读的
	Deferred: function( func ) {
		var tuples = [
				[ "resolve", "done", jQuery.Callbacks("once memory"), "resolved" ],
				[ "reject", "fail", jQuery.Callbacks("once memory"), "rejected" ],
				[ "notify", "progress", jQuery.Callbacks("memory") ]
			],
			state = "pending",
			//扩展的promise对象
			promise = {
				state: function() {
					return state;
				},
				always: function() {
					deferred.done( arguments ).fail( arguments );
					return this;
				},
				then: function( /* fnDone, fnFail, fnProgress */ ) {
					var fns = arguments;
					return jQuery.Deferred(function( newDefer ) {
						jQuery.each( tuples, function( i, tuple ) {
							var fn = jQuery.isFunction( fns[ i ] ) && fns[ i ];
							// deferred[ done | fail | progress ] for forwarding actions to newDefer
							deferred[ tuple[1] ](function() {
								var returned = fn && fn.apply( this, arguments );
								if ( returned && jQuery.isFunction( returned.promise ) ) {
									returned.promise()
										.done( newDefer.resolve )
										.fail( newDefer.reject )
										.progress( newDefer.notify );
								} else {
									newDefer[ tuple[ 0 ] + "With" ]( this === promise ? newDefer.promise() : this, fn ? [ returned ] : arguments );
								}
							});
						});
						fns = null;
					}).promise();
				},
				// Get a promise for this deferred
				// If obj is provided, the promise aspect is added to the object
				promise: function( obj ) {
					return obj != null ? jQuery.extend( obj, promise ) : promise;
				}
			},
			//内部deferred对象
			deferred = {};

		 //定义管道风格的接口pipe,API别名
		promise.pipe = promise.then;

		//逐个添加所有的接口到deferred对象上
		jQuery.each( tuples, function( i, tuple ) {
			var list = tuple[ 2 ],
				stateString = tuple[ 3 ];

			// promise[ done | fail | progress ] = list.add
			promise[ tuple[1] ] = list.add;

			// Handle state
			if ( stateString ) {
				list.add(function() {
					// state = [ resolved | rejected ]
					state = stateString;

				// [ reject_list | resolve_list ].disable; progress_list.lock
				}, tuples[ i ^ 1 ][ 2 ].disable, tuples[ 2 ][ 2 ].lock );
			}

			// deferred[ resolve | reject | notify ]
			deferred[ tuple[0] ] = function() {
				deferred[ tuple[0] + "With" ]( this === deferred ? promise : this, arguments );
				return this;
			};
			deferred[ tuple[0] + "With" ] = list.fireWith;
		});

		//转成成promise对象
		promise.promise( deferred );

		//如果传递的参数是函数，直接运行
		if ( func ) {
			func.call( deferred, deferred );
		}

		return deferred;
	},

	//when就是一个合集的处理
	//可以收集多个异步操作，合并成功后处理
	//同时也可以绑定Promise 对象的其它方法，如 defered.then
	//所以when内部必须要创建一个deferred对象
	when: function( subordinate /* , ..., subordinateN */ ) {
		var i = 0,
			resolveValues = slice.call( arguments ),
			length = resolveValues.length,

			// the count of uncompleted subordinates
			remaining = length !== 1 || ( subordinate && jQuery.isFunction( subordinate.promise ) ) ? length : 0,

			// the master Deferred. If resolveValues consist of only a single Deferred, just use that.
			deferred = remaining === 1 ? subordinate : jQuery.Deferred(),

			// Update function for both resolve and progress values
			updateFunc = function( i, contexts, values ) {
				return function( value ) {
					contexts[ i ] = this;
					values[ i ] = arguments.length > 1 ? slice.call( arguments ) : value;
					if ( values === progressValues ) {
						deferred.notifyWith( contexts, values );
					} else if ( !( --remaining ) ) {
						deferred.resolveWith( contexts, values );
					}
				};
			},

			progressValues, progressContexts, resolveContexts;

		// Add listeners to Deferred subordinates; treat others as resolved
		if ( length > 1 ) {
			progressValues = new Array( length );
			progressContexts = new Array( length );
			resolveContexts = new Array( length );
			for ( ; i < length; i++ ) {
				if ( resolveValues[ i ] && jQuery.isFunction( resolveValues[ i ].promise ) ) {
					resolveValues[ i ].promise()
						.done( updateFunc( i, resolveContexts, resolveValues ) )
						.fail( deferred.reject )
						.progress( updateFunc( i, progressContexts, progressValues ) );
				} else {
					--remaining;
				}
			}
		}

		// If we're not waiting on anything, resolve the master
		if ( !remaining ) {
			deferred.resolveWith( resolveContexts, resolveValues );
		}

		return deferred.promise();
	}
});

