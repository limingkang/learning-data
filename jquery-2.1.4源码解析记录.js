	//匹配属性选择器，例如：input[arr^='val'] 以下是分析：
    //捕获组1：characterEncoding ：匹配例子中arr
    //捕获组2：([*^$|!~]?=)：匹配例子中^=，其中？表示匹配0次或者一次
    //捕获组3：['\"]:匹配例子中'
    //捕获组4：(?:\\\\.|[^\\\\])*? 解释一下这个外层捕获\和后面的任意数量字符或非\字符,内层有?:是不捕获的，匹配例子中的val
    //捕获组5：匹配identifier，匹配[id=#a]的情况
    //当你要使用\转义别人的时候\得先被转义
    //当?紧随任何其他限定符（*、+、?、{n}、{n,}、{n,m}）之后时，匹配模式是“非贪心的”。“非贪心的”模式匹配搜索到的、尽可能短的字符串，而默认的“贪心的”模式匹配搜索到的、尽可能长的字符串。例如，在字符串“oooo”中，“o+?”只匹配单个“o”，而“o+”匹配所有“o”
	attributes = "\\[" + whitespace + "*(" + characterEncoding + ")(?:" + whitespace +
		// 先匹配中括号，之后是0个或者多个空格，之后就是arr在之后又是0或者多个空格······
		"*([*^$|!~]?=)" + whitespace +
		"*(?:'((?:\\\\.|[^\\\\'])*)'|\"((?:\\\\.|[^\\\\\"])*)\"|(" + identifier + "))|)" + whitespace +
		"*\\]",
    
    // 匹配伪类表达式,例如li:nth-child(2n+1)
    // 捕获组1：characterEncoding ：匹配例子中nth-child
    // 捕获组2:匹配例子中的2n+1
    // 捕获组3:匹配' or "
    // 捕获组4:匹配\和后面任何字符或非\字符
    // 捕获组5:匹配不再' or "内的\和后面任意字符或非\()[]字符或attributes
	pseudos = ":(" + characterEncoding + ")(?:\\((" +
		
		"('((?:\\\\.|[^\\\\'])*)'|\"((?:\\\\.|[^\\\\\"])*)\")|" +

		"((?:\\\\.|[^\\\\()[\\]]|" + attributes + ")*)|" +

		".*" +
		")\\)|)",

	//正则有两个参数，g表示执行全局匹配（查找所有匹配而非在找到第一个匹配后停止）
	//i表示执行对大小写不敏感的匹配
    //m表示执行多行匹配
    //使用new正则的方法，要注意使用双重转义[abc]表示匹配abc中的任何一个，[^abc]表示除了abc的任何字符
	rwhitespace = new RegExp( whitespace + "+", "g" ),

	// 去掉两端空白和字符串中的反斜杠（如果连续两个去掉一个）正则
	rtrim = new RegExp( "^" + whitespace + "+|((?:^|[^\\\\])(?:\\\\.)*)" + whitespace + "+$", "g" ),
   
    //并联选择器的正则,就是用逗号分隔的那种形式
	rcomma = new RegExp( "^" + whitespace + "*," + whitespace + "*" ),
	
	 //关系选择器正则
	rcombinators = new RegExp( "^" + whitespace + "*([>+~]|" + whitespace + ")" + whitespace + "*" ),

	//可能是自定义属性正则
	rattributeQuotes = new RegExp( "=" + whitespace + "*([^\\]'\"]*?)" + whitespace + "*\\]", "g" ),
    
    //伪类正则字符串
	rpseudo = new RegExp( pseudos ),
	ridentifier = new RegExp( "^" + identifier + "$" ),

	matchExpr = {
		"ID": new RegExp( "^#(" + characterEncoding + ")" ),
		"CLASS": new RegExp( "^\\.(" + characterEncoding + ")" ),
		"TAG": new RegExp( "^(" + characterEncoding.replace( "w", "w*" ) + ")" ),
		"ATTR": new RegExp( "^" + attributes ),
		"PSEUDO": new RegExp( "^" + pseudos ),
		"CHILD": new RegExp( "^:(only|first|last|nth|nth-last)-(child|of-type)(?:\\(" + whitespace +
			"*(even|odd|(([+-]|)(\\d*)n|)" + whitespace + "*(?:([+-]|)" + whitespace +
			"*(\\d+)|))" + whitespace + "*\\)|)", "i" ),
		"bool": new RegExp( "^(?:" + booleans + ")$", "i" ),
		"needsContext": new RegExp( "^" + whitespace + "*[>+~]|:(even|odd|eq|gt|lt|nth|first|last)(?:\\(" +
			whitespace + "*((?:-\\d)?\\d*)" + whitespace + "*\\)|)(?=[^-]|$)", "i" )
	},

	rinputs = /^(?:input|select|textarea|button)$/i,
	rheader = /^h\d$/i,
    //匹配原生函数
	rnative = /^[^{]+\{\s*\[native \w/,

	//仅仅单个id或tag、class选择器正则（用来快速解析并获取元素）
	rquickExpr = /^(?:#([\w-]+)|(\w+)|\.([\w-]+))$/,

	rsibling = /[+~]/,
	rescape = /'|\\/g,

	//css转义，配合funescape使用 如：string.replace(runescape, funescape);
	//.表示任何字符的通配符
	runescape = new RegExp( "\\\\([\\da-f]{1,6}" + whitespace + "?|(" + whitespace + ")|.)", "ig" ),
	funescape = function( _, escaped, escapedWhitespace ) {
		var high = "0x" + escaped - 0x10000;
		//解决错误的数值型关于"0x"解释说明
		return high !== high || escapedWhitespace ?
			escaped :
			high < 0 ?
				// 该方法是string类型的静态方法，他将根据 Unicode 来输出实际字符值
				String.fromCharCode( high + 0x10000 ) :
				//按二进制形式把所有的数字向右移动对应的位数，低位移出(舍弃)，高位的空位补符号位，即正数补零，负数补1
				String.fromCharCode( high >> 10 | 0xD800, high & 0x3FF | 0xDC00 );
	},

	//当访问document变量时,IE将抛出"permission denied"错误
	//被应用于iframe框架中 
	unloadHandler = function() {
		setDocument();
	};

//把NodeList转换为数组，因为数组比集合效率高
try {
	push.apply(
		//这样的话arr就为preferredDoc.childNodes的两个数组叠加
		(arr = slice.call( preferredDoc.childNodes )),
		preferredDoc.childNodes
	);
	//检测上面的push.apply方法是否成功
	arr[ preferredDoc.childNodes.length ].nodeType;
} catch ( e ) {
	push = { apply: arr.length ?
		//push_native就是上面的arr.push方法的引用
		function( target, els ) {
			push_native.apply( target, slice.call(els) );
		} :
		//这里大概是在重写push.apply方法,上面用到了
		function( target, els ) {
			var j = target.length,
				i = 0;
			// Can't trust NodeList.length
			while ( (target[j++] = els[i++]) ) {}
			target.length = j - 1;
		}
	};
}
//基本后面的好多选择方法最后走的都是sizzle方法
//selector css选择器, context执行匹配的最初的上下文，results已经匹配出的部分结果集，seed筛选集也就是初始集合
//对于单一选择器，且是ID、Tag、Class三种类型之一，则直接获取并返回结果
//对于支持querySelectorAll方法的浏览器，通过执行querySelectorAll方法获取并返回匹配的DOM元素
//除上之外则调用select方法获取并返回匹配的DOM元素
function Sizzle( selector, context, results, seed ) {
	var match, elem, m, nodeType,
    //执行qsa方法时候所需要的参数定义
		i, groups, old, nid, newContext, newSelector;
    //ownerDocument返回根节点document即nodeType为9的时候
    //上面已经有preferredDoc=window.document
    //如果匹配的上下文不是docunment那么就用setDocument方法进行初始化
	if ( ( context ? context.ownerDocument || context : preferredDoc ) !== document ) {
		setDocument( context );
	}

	context = context || document;
	results = results || [];
	nodeType = context.nodeType;

	if ( typeof selector !== "string" || !selector ||
		nodeType !== 1 && nodeType !== 9 && nodeType !== 11 ) {
        //其中11代表DocumentFragment，是轻量级的 Document 对象，能够容纳文档的某个部分
        //上述都不满足那么直接放回结果，是空数组
		return results;
	}

	if ( !seed && documentIsHTML ) {
          //如果筛选集不存在并且当前过滤的是html文档
		 // 如果仅仅是id或class或tag用原生函数
		if ( nodeType !== 11 && (match = rquickExpr.exec( selector )) ) {
			// 在只是单个的情况下迅速匹配如Sizzle("#ID")
			//注意exec方法返回的是一个数组,返回第一个匹配的字符串内容，此时的数组仅有一个元素,没匹配到就返回null
			//因为正则中第一是找id所以match[1]可以取到#号后面的字符串，第二个是tag标签获取，以此类推
			if ( (m = match[1]) ) {
				//如果context是文档节点
				if ( nodeType === 9 ) {
					elem = context.getElementById( m );
					if ( elem && elem.parentNode ) {
						//一些老版本的浏览器会把name当作ID来处理，
      				    //返回不正确的结果，所以需要再一次对比返回节点的ID属性
						if ( elem.id === m ) {
							results.push( elem );
							return results;
						}
					} else {
						return results;
					}
				} else {
					// contains(context, elem)用来确认获取的elem是否是当前context对象的子对象
					if ( context.ownerDocument && (elem = context.ownerDocument.getElementById( m )) &&
						contains( context, elem ) && elem.id === m ) {
						results.push( elem );
						return results;
					}
				}

			// 快速匹配: Sizzle("TAG")
			} else if ( match[2] ) {
				push.apply( results, context.getElementsByTagName( selector ) );
				return results;

			//快速匹配: Sizzle(".CLASS")
			//处理class类型选择器，如：.class
   			//m = match[3]：有效的class类型选择器
   			//support.getElementsByClassName 该选择器的div支持getElementsByClassName
   			//context.getElementsByClassName 当前上下文节点有getElementsByClassName方法
			} else if ( (m = match[3]) && support.getElementsByClassName ) {
				push.apply( results, context.getElementsByClassName( m ) );
				return results;
			}
		}

        //若浏览器支持querySelectorAll方法且选择器符合querySelectorAll调用标准，则执行if内语句体
        //在这里的检查仅仅是简单匹配
        //第一次调用Sizzle时，rbuggyQSA为空
        //if语句体内对当前context对象的id的赋值与恢复，是用来修正querySelectorAll的一个BUG
        //该BUG会在某些情况下把当前节点（context）也作为结果返回回来。
        //具体方法是，在现有的选择器前加上一个属性选择器：[id=XXX]，
        //XXX 为context的id，若context本身没有设置id，则给个默认值expando。
		if ( support.qsa && (!rbuggyQSA || !rbuggyQSA.test( selector )) ) {
			nid = old = expando;	//其中expando = "sizzle" + 1 * new Date(),用于缓存数据的唯一标示
			newContext = context;
			//若context是不是元素节点，则newSelector取自selector，否则为false
			newSelector = nodeType !== 1 && selector;

			if ( nodeType === 1 && context.nodeName.toLowerCase() !== "object" ) {
				groups = tokenize( selector );

				if ( (old = context.getAttribute("id")) ) {
					// rescape = /'|\/g,
     				//这里将old中的单引号、竖杠、反斜杠前加一个反斜杠
     				//old.replace(rescape, \$&)代码中的$&代表匹配项
					nid = old.replace( rescape, "\\$&" );
				} else {
					context.setAttribute( "id", nid );
				}
				nid = "[id='" + nid + "'] ";
                //重新组合新的选择器
				i = groups.length;
				while ( i-- ) {
					groups[i] = nid + toSelector( groups[i] );
				}
                //rsibling = new RegExp(whitespace + *[+~])
                //rsibling用于判定选择器是否存在兄弟关系符
                //若包含+~符号，则取context的父节点作为当前节点
				newContext = rsibling.test( selector ) && testContext( context.parentNode ) || context;
				newSelector = groups.join(",");
			}

			if ( newSelector ) {
                 //这里之所以需要用try...catch，
                 //是因为jquery所支持的一些选择器是querySelectorAll所不支持的，
                 //当使用这些选择器时，querySelectorAll会报非法选择器，
                 //故需要jquery自身去实现。
				try {
					// 将querySelectorAll获取的结果并入results，而后返回resulsts
					push.apply( results,
						newContext.querySelectorAll( newSelector )
					);
					return results;
				} catch(qsaError) {
				} finally {
					if ( !old ) {
						context.removeAttribute("id");
					}
				}
			}
		}
	}
    // 除上述快捷方式和调用querySelectorAll方式直接获取结果外，其余都需调用select来获取结果
    //上述rtrim正则表达式的作用是去掉selector两边的空白和字符串中的反斜杠（如果连续两个去掉一个）
    //var str="abc正则mdb";
    // var reg=/(\w)正则(\w)/g;
    // var str=str.replace(reg, "$1$2");
    // console.log(str);其中"c正则m"会被换为$1$2，那么$1代表正则中第一子表达式所匹配的内容$2就代表第二个
    //也就是表示第一个小括号匹配和第二个小括号匹配，最终输出abccmmdb
	return select( selector.replace( rtrim, "$1" ), context, results, seed );
}
//创建缓存函数（缓存大小默认为50,可以自己设置）
//这里利用了闭包（私有变量、变量一直保存在内存中）
//当缓存太多的时候，就删除前面的值，相当于先进后出类型
function createCache() {
	var keys = [];

	function cache( key, value ) {
		
		if ( keys.push( key + " " ) > Expr.cacheLength ) {
		
			delete cache[ keys.shift() ];
		}
		return (cache[ key + " " ] = value);
	}
	return cache;
}
//给创建的函数以特定的标识符，并再返回改函数
function markFunction( fn ) {
	fn[ expando ] = true;
	return fn;
}
// 用于做各种特征检测，比如是否支持某个API，API支持是否完美
// 这里div看上去不可能有parentNode，因为感觉它没有被插入DOM树。但由于被传入的函数fn很有可能会将这个div插入DOM树，所有必须执行此操作。
//最后要将div的引用置空，方便回收
function assert( fn ) {
	var div = document.createElement("div");
    //此处用try-catch的原因是：被传入的fn很有可能是会报错的。因为fn中用的方法或属性很可能不被当前浏览器所支持
	try {
		return !!fn( div );
	} catch (e) {
		return false;
	} finally {
	//finally是不管出不出错都会执行，如果try或者catch中有return那么他将会在return之后执行
	// 如果被创建的div存在parentNode，则将当前div删除。　　
		if ( div.parentNode ) {
			div.parentNode.removeChild( div );
		}
		div = null;
	}
}
//handler是一个处理函数，attrs是一个属性也就是一个字符串
//就是给该属性的分割后的数组都加上同一个数组
function addHandle( attrs, handler ) {
	var arr = attrs.split("|"),
		i = attrs.length;   //字符串的长度

	while ( i-- ) {
		Expr.attrHandle[ arr[i] ] = handler;
	}
}
//该方法是用来检测是否是兄弟节点的关系，以及谁前谁后的问题,就是兄弟节点的排序
//~就是按位取反的意思，如果32位开头第一个是1，所以这是一个负数，将二进制转换成负数，需要先反码之后再加1转换成十进制再加上负号
//从IE4到IE11，都有sourceIndex属性，这个属性表示了元素在DOM树的顺序，比较元素和父元素的sourceIndex的差值就很容易知道元素是第几个子元素了。
//也就是nodeType为1的节点，文本节点、注释节点等将不被统计。如果需要计算所有节点在内，则不能适用sourceIndex，因为该属性只针对Element. 
//如果a在b前面的话返回-1
function siblingCheck( a, b ) {
	var cur = b && a,
		diff = cur && a.nodeType === 1 && b.nodeType === 1 &&
			( ~b.sourceIndex || MAX_NEGATIVE ) -
			( ~a.sourceIndex || MAX_NEGATIVE );

	if ( diff ) {
		return diff;
	}
	if ( cur ) {
		while ( (cur = cur.nextSibling) ) {
			if ( cur === b ) {
				return -1;
			}
		}
	}

	return a ? 1 : -1;
}
//创建一个伪类的过滤函数,此方法是根据表单元素的type值生成
