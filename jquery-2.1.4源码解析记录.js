/*
tokenize方法是选择器解析的核心函数，它将选择器转换成两级数组groups
举例：
    若选择器为“div.class,span”，则解析后的结果为：
    group[0][0] = {type:'TAG',value:'div',matches:match}
    group[0][1] = {type:'CLASS',value:'.class',matches:match}
    group[1][0] = {type:'TAG',value:'span',matches:match}
    由上述结果可以看出，groups的每一个元素以逗号分隔的选择器块的解析结果，
tokenize方法完成如下两个主要任务：
    1、解析选择器
    2、将解析结果存入缓存中，以备后用
参数selector 待解析的选择器字符串
参数parseOnly 为true时，说明本次调用是匹配子选择器
举个例子：
    若初始选择器为"div:not(.class:not(:eq(4))):eq(3)"
    代码首先匹配出TAG选择器div，
    之后匹配出的pseudo选择器字符串是:not(.class:not(:eq(4))):eq(3)，
    代码会把“.class:not(:eq(4))):eq(3”作为not的括号内的值进一步进行解析，
    此时代码在调用tokenize解析时，parseOnly参数会传入true.
*/
tokenize = Sizzle.tokenize = function( selector, parseOnly ) {
	var matched, match, tokens, type,
		soFar, groups, preFilters,
		//这里的soFar是表示目前还未分析的字符串剩余部分
        //groups表示目前已经匹配到的规则组,有多少个并联选择器，里面就有多少个数组，数组里面是拥有value与type的对象
		// 获取缓存中的结果
		cached = tokenCache[ selector + " " ];
    //如果有缓存的情况下，若是对初始选择器解析（parseOnly!=true），则返回缓存结果，
    // 若不是，则返回0
	if ( cached ) {
		return parseOnly ? 0 : cached.slice( 0 );
	}
    // 由于字符串在javascript中不是作为对象来处理的，
    // 所以通过赋值，代码就自动复制了一个新字符串给了soFar，
    // 这样，对soFar的任何处理都不会影响selector的原有数据
	soFar = selector;
	groups = [];
	// 此处赋值，仅仅用于减少后续代码字数，缩短执行路径
	preFilters = Expr.preFilter;

	while ( soFar ) {

		// rcomma用来判定是否存在多个选择器块，即用逗号隔开的多个并列的选择器
		// !matched：若是第一次执行循环体，则为true；否则为false。
        //这里matched即作为是否第一次执行循环体的标识，
        //也作为本次循环中soFar是否以非法字符串（即非合法单一选择器）开头的标志。
        //(match = rcomma.exec(soFar)：获取符合rcomma的匹配项
		if ( !matched || (match = rcomma.exec( soFar )) ) {
			if ( match ) {
				/*
                剔除掉第一个逗号及之前的所有字符
                举个例子：
                   若初始选择器为："div.news,span.closed"，
                   在解析过程中，首先由后续代码解析完毕div.news，剩下",span.closed"
                   在循环体内执行到这里时，将逗号及之前之后连续的空白（match[0]）删除掉，
                   使soFar变成"span.closed"，继续执行解析过程
                   在这里，若初始选择器的最后一个非空白字符是逗号，
                   那么执行下面代码时soFar不变，即soFar.slice(match[0].length)返回空字符串，
                   故最终返回的是||后面的soFar
                 */
				soFar = soFar.slice( match[0].length ) || soFar;
			}
			//在第一次执行循环体或者遇到逗号分割符时，将tokens赋值为一个空数组，同时压入groups数组
			groups.push( (tokens = []) );
		}

		matched = false;

		if ( (match = rcombinators.exec( soFar )) ) {
			/*
             * 将match[0]移除match数组，同时将它赋予matched
             * 若原本关系符两边带有空格，则此时match[0]与matched是不相等的
             * 举个例子：
             * 若s oFar= " + .div";
             * 执行match = rcombinators.exec(soFar)后，
             * match[0] = " + "，而match[1]="+";
             * 执行完matched = match.shift()后，
             * matched=" + "，而match[0]="+";
             */
			matched = match.shift();
			tokens.push({
				value: matched,
				//下面match[0].replace(rtrim, " ")的作用是将match[0]左右两边的空白替换为空格
				type: match[0].replace( rtrim, " " )
			});
			// 将关系符之后的字符串赋予soFar，继续解析
            soFar = soFar.slice(matched.length);
			soFar = soFar.slice( matched.length );
		}

		/*
         * 下面通过for语句对soFar逐一匹配ID、TAG、CLASS、CHILD、ATTR、PSEUDO类型的选择器
         * 若匹配到了，则先调用该类型选择器对应的预过滤函数，
         * 然后，将结果压入tokens数组，继续本次循环。
         */
		for ( type in Expr.filter ) {
			/*
             * match = matchExpr[type].exec(soFar)：对soFar调用type类型的正则表达式对soFar进行匹配，
             *  并将匹配结果赋予match。若未匹配到数据，则match为undefined。
             * !preFilters[type]：若不存在type类型的预过滤函数，则为true
             * match = preFilters[type](match)：执行预过滤，并将结果返回给match
             * 
             */
			if ( (match = matchExpr[ type ].exec( soFar )) && (!preFilters[ type ] ||
				(match = preFilters[ type ]( match ))) ) {
				// 将match[0]移除match数组，同时将它赋予matched
				matched = match.shift();
			    // 将匹配结果压入tokens数组中
				tokens.push({
					value: matched,
					type: type,
					matches: match
				});
				// 将匹配结果之后的字符串赋予soFar，继续解析
				soFar = soFar.slice( matched.length );
			}
		}
        /*
         * 若matched==false，
         * 则说明本次循环没有有效的选择器（包括关系符和id、class等类型选择器）
         * 因此，解析到当前位置遗留下来的soFar是非法的选择器字符串
         * 跳出while循环体
         */
		if ( !matched ) {
			break;
		}
	}

	/*
    若不是对初始选择器字符串进行解析（!parseOnly==true），
    则返回soFar.length，此时的soFar.length代表连续有效的选择器最终位置，
    后续文章将以实例进行说明
    若是对初始选择器字符串进行解析，则看soFar是否还有字符，
    若是，则执行Sizzle.error(selector)抛出异常；
    若不是，则执行tokenCache(selector, groups).slice(0)将结果压入缓存，并返回结果的副本。
    */
	return parseOnly ?
		soFar.length :
		soFar ?
			Sizzle.error( selector ) :
			// Cache the tokens
			tokenCache( selector, groups ).slice( 0 );
};
//返回已经匹配好的数组中value合集，也就是selector的字符串
function toSelector( tokens ) {
	var i = 0,
		len = tokens.length,
		selector = "";
	for ( ; i < len; i++ ) {
		selector += tokens[i].value;
	}
	return selector;
}

function addCombinator( matcher, combinator, base ) {
	var dir = combinator.dir,
		checkNonElements = base && dir === "parentNode",
		doneName = done++;

	return combinator.first ?
		// Check against closest ancestor/preceding element
		function( elem, context, xml ) {
			while ( (elem = elem[ dir ]) ) {
				if ( elem.nodeType === 1 || checkNonElements ) {
					return matcher( elem, context, xml );
				}
			}
		} :

		// Check against all ancestor/preceding elements
		function( elem, context, xml ) {
			var oldCache, outerCache,
				newCache = [ dirruns, doneName ];

			// We can't set arbitrary data on XML nodes, so they don't benefit from dir caching
			if ( xml ) {
				while ( (elem = elem[ dir ]) ) {
					if ( elem.nodeType === 1 || checkNonElements ) {
						if ( matcher( elem, context, xml ) ) {
							return true;
						}
					}
				}
			} else {
				while ( (elem = elem[ dir ]) ) {
					if ( elem.nodeType === 1 || checkNonElements ) {
						outerCache = elem[ expando ] || (elem[ expando ] = {});
						if ( (oldCache = outerCache[ dir ]) &&
							oldCache[ 0 ] === dirruns && oldCache[ 1 ] === doneName ) {

							// Assign to newCache so results back-propagate to previous elements
							return (newCache[ 2 ] = oldCache[ 2 ]);
						} else {
							// Reuse newcache so results back-propagate to previous elements
							outerCache[ dir ] = newCache;

							// A match means we're done; a fail means we have to keep checking
							if ( (newCache[ 2 ] = matcher( elem, context, xml )) ) {
								return true;
							}
						}
					}
				}
			}
		};
}
//该函数返回一个函数，用来判定传入的elem是否符合匹配执行函数数组matchers，若不满足返回false，否则返回true。
//若matchers仅有一个元素，则返回该元素本身，否则返回一个新函数——即代码中的function(elem, context, xml)函数。
//返回函数效果有点类似于context.filter(selectors)，当然，其结果仅仅返回true或false，而非jQuery对象
//matchers——数组，每个元素都是非伪类的匹配器执行函数
//在实际执行过程中，div.map span:lt(10)，其中div和,map的匹配执行函数就会作为matchers的两个元素传入elementMatcher函数来过滤span节点的父节点是否满足要求
//elem——待检查的单个节点元素
//context——执行整个选择器字符串匹配的上下文节点，大部分时候是没有用途
//xml——当前搜索对象是HTML还是XML文档，若是HTML，则xml参数为false
function elementMatcher( matchers ) {
	return matchers.length > 1 ?
		function( elem, context, xml ) {
			var i = matchers.length;
			while ( i-- ) {
				if ( !matchers[i]( elem, context, xml ) ) {
					return false;
				}
			}
			return true;
		} :
		matchers[0];
}

function multipleContexts( selector, contexts, results ) {
	var i = 0,
		len = contexts.length;
	for ( ; i < len; i++ ) {
		Sizzle( selector, contexts[i], results );
	}
	return results;
}

function condense( unmatched, map, filter, context, xml ) {
	var elem,
		newUnmatched = [],
		i = 0,
		len = unmatched.length,
		mapped = map != null;

	for ( ; i < len; i++ ) {
		if ( (elem = unmatched[i]) ) {
			if ( !filter || filter( elem, context, xml ) ) {
				newUnmatched.push( elem );
				if ( mapped ) {
					map.push( i );
				}
			}
		}
	}

	return newUnmatched;
}
//依据preFilter和selector获得匹配结果；然后执行matcher函数获取匹配结果；之后，执行postFilter函数，最后，依据postFinder和postSelector获取匹配结果
function setMatcher( preFilter, selector, matcher, postFilter, postFinder, postSelector ) {
	if ( postFilter && !postFilter[ expando ] ) {
		postFilter = setMatcher( postFilter );
	}
	if ( postFinder && !postFinder[ expando ] ) {
		postFinder = setMatcher( postFinder, postSelector );
	}
	return markFunction(function( seed, results, context, xml ) {
		var temp, i, elem,
			preMap = [],
			postMap = [],
			preexisting = results.length,

			// Get initial elements from seed or context
			elems = seed || multipleContexts( selector || "*", context.nodeType ? [ context ] : context, [] ),

			// Prefilter to get matcher input, preserving a map for seed-results synchronization
			matcherIn = preFilter && ( seed || !selector ) ?
				condense( elems, preMap, preFilter, context, xml ) :
				elems,

			matcherOut = matcher ?
				// If we have a postFinder, or filtered seed, or non-seed postFilter or preexisting results,
				postFinder || ( seed ? preFilter : preexisting || postFilter ) ?

					// ...intermediate processing is necessary
					[] :

					// ...otherwise use results directly
					results :
				matcherIn;

		// Find primary matches
		if ( matcher ) {
			matcher( matcherIn, matcherOut, context, xml );
		}

		// Apply postFilter
		if ( postFilter ) {
			temp = condense( matcherOut, postMap );
			postFilter( temp, [], context, xml );

			// Un-match failing elements by moving them back to matcherIn
			i = temp.length;
			while ( i-- ) {
				if ( (elem = temp[i]) ) {
					matcherOut[ postMap[i] ] = !(matcherIn[ postMap[i] ] = elem);
				}
			}
		}

		if ( seed ) {
			if ( postFinder || preFilter ) {
				if ( postFinder ) {
					// Get the final matcherOut by condensing this intermediate into postFinder contexts
					temp = [];
					i = matcherOut.length;
					while ( i-- ) {
						if ( (elem = matcherOut[i]) ) {
							// Restore matcherIn since elem is not yet a final match
							temp.push( (matcherIn[i] = elem) );
						}
					}
					postFinder( null, (matcherOut = []), temp, xml );
				}

				// Move matched elements from seed to results to keep them synchronized
				i = matcherOut.length;
				while ( i-- ) {
					if ( (elem = matcherOut[i]) &&
						(temp = postFinder ? indexOf( seed, elem ) : preMap[i]) > -1 ) {

						seed[temp] = !(results[temp] = elem);
					}
				}
			}

		// Add elements to results, through postFinder if defined
		} else {
			matcherOut = condense(
				matcherOut === results ?
					matcherOut.splice( preexisting, matcherOut.length ) :
					matcherOut
			);
			if ( postFinder ) {
				postFinder( null, results, matcherOut, xml );
			} else {
				push.apply( results, matcherOut );
			}
		}
	});
}
//针对一个块选择器生成执行函数，所谓块选择器就是不包含逗号分隔的选择器字符串。
//函数针对不同类型的选择器产生不同的执行函数。若包含伪类，则返回setMatcher，否则返回的是elementMatcher，代码通过鉴别matcher是否包含expando属性来区别setMatcher和elementMatcher：
function matcherFromTokens( tokens ) {
	var checkContext, matcher, j,
		len = tokens.length,
		leadingRelative = Expr.relative[ tokens[0].type ],
		implicitRelative = leadingRelative || Expr.relative[" "],
		i = leadingRelative ? 1 : 0,

		// The foundational matcher ensures that elements are reachable from top-level context(s)
		matchContext = addCombinator( function( elem ) {
			return elem === checkContext;
		}, implicitRelative, true ),
		matchAnyContext = addCombinator( function( elem ) {
			return indexOf( checkContext, elem ) > -1;
		}, implicitRelative, true ),
		matchers = [ function( elem, context, xml ) {
			var ret = ( !leadingRelative && ( xml || context !== outermostContext ) ) || (
				(checkContext = context).nodeType ?
					matchContext( elem, context, xml ) :
					matchAnyContext( elem, context, xml ) );
			// Avoid hanging onto element (issue #299)
			checkContext = null;
			return ret;
		} ];

	for ( ; i < len; i++ ) {
		if ( (matcher = Expr.relative[ tokens[i].type ]) ) {
			matchers = [ addCombinator(elementMatcher( matchers ), matcher) ];
		} else {
			matcher = Expr.filter[ tokens[i].type ].apply( null, tokens[i].matches );

			// Return special upon seeing a positional matcher
			if ( matcher[ expando ] ) {
				// Find the next relative operator (if any) for proper handling
				j = ++i;
				for ( ; j < len; j++ ) {
					if ( Expr.relative[ tokens[j].type ] ) {
						break;
					}
				}
				return setMatcher(
					i > 1 && elementMatcher( matchers ),
					i > 1 && toSelector(
						// If the preceding token was a descendant combinator, insert an implicit any-element `*`
						tokens.slice( 0, i - 1 ).concat({ value: token123s[ i - 2 ].type === " " ? "*" : "" })
					).replace( rtrim, "$1" ),
					matcher,
					i < j && matcherFromTokens( tokens.slice( i, j ) ),
					j < len && matcherFromTokens( (tokens = tokens.slice( j )) ),
					j < len && toSelector( tokens )
				);
			}
			matchers.push( matcher );
		}
	}

	return elementMatcher( matchers );
}
//将不同的块选择器生成的最终执行函数，该函数还负责将最终结果过滤掉重复对象。
function matcherFromGroupMatchers( elementMatchers, setMatchers ) {
	var bySet = setMatchers.length > 0,
		byElement = elementMatchers.length > 0,
		superMatcher = function( seed, context, xml, results, outermost ) {
			var elem, j, matcher,
				matchedCount = 0,
				i = "0",
				unmatched = seed && [],
				setMatched = [],
				contextBackup = outermostContext,
				// We must always have either seed elements or outermost context
				elems = seed || byElement && Expr.find["TAG"]( "*", outermost ),
				// Use integer dirruns iff this is the outermost matcher
				dirrunsUnique = (dirruns += contextBackup == null ? 1 : Math.random() || 0.1),
				len = elems.length;

			if ( outermost ) {
				outermostContext = context !== document && context;
			}

			// Add elements passing elementMatchers directly to results
			// Keep `i` a string if there are no elements so `matchedCount` will be "00" below
			// Support: IE<9, Safari
			// Tolerate NodeList properties (IE: "length"; Safari: <number>) matching elements by id
			for ( ; i !== len && (elem = elems[i]) != null; i++ ) {
				if ( byElement && elem ) {
					j = 0;
					while ( (matcher = elementMatchers[j++]) ) {
						if ( matcher( elem, context, xml ) ) {
							results.push( elem );
							break;
						}
					}
					if ( outermost ) {
						dirruns = dirrunsUnique;
					}
				}

				// Track unmatched elements for set filters
				if ( bySet ) {
					// They will have gone through all possible matchers
					if ( (elem = !matcher && elem) ) {
						matchedCount--;
					}

					// Lengthen the array for every element, matched or not
					if ( seed ) {
						unmatched.push( elem );
					}
				}
			}

			// Apply set filters to unmatched elements
			matchedCount += i;
			if ( bySet && i !== matchedCount ) {
				j = 0;
				while ( (matcher = setMatchers[j++]) ) {
					matcher( unmatched, setMatched, context, xml );
				}

				if ( seed ) {
					// Reintegrate element matches to eliminate the need for sorting
					if ( matchedCount > 0 ) {
						while ( i-- ) {
							if ( !(unmatched[i] || setMatched[i]) ) {
								setMatched[i] = pop.call( results );
							}
						}
					}

					// Discard index placeholder values to get only actual matches
					setMatched = condense( setMatched );
				}

				// Add matches to results
				push.apply( results, setMatched );

				// Seedless set matches succeeding multiple successful matchers stipulate sorting
				if ( outermost && !seed && setMatched.length > 0 &&
					( matchedCount + setMatchers.length ) > 1 ) {

					Sizzle.uniqueSort( results );
				}
			}

			// Override manipulation of globals by nested matchers
			if ( outermost ) {
				dirruns = dirrunsUnique;
				outermostContext = contextBackup;
			}

			return unmatched;
		};

	return bySet ?
		markFunction( superMatcher ) :
		superMatcher;
}
