//主要功能是根据各个浏览器对原生方法的支持情况对当前浏览器是否支持某方法进行判别
//并且重写了浏览器的好多方法做了兼容，所以页面加载之前就要先运行它一次
// node就是上面sizzle函数的第二个参数
// 如果传入了node，则拿到node所在的document，如果node没有ownerDocument，说明它本身就是document； 如果没有传入node，则使用preferredDoc（它的值是window.document）
setDocument = Sizzle.setDocument = function( node ) {
		var hasCompare, parent,
			doc = node ? node.ownerDocument || node : preferredDoc;

		//文档的一个根就是<html>,在DOM中可以使用document.documentElement来访问它，它就是整个节点树的根节点
		//注意元素节点的documentElement返回的是undefined
		if ( doc === document || doc.nodeType !== 9 || !doc.documentElement ) {
			return document;
		}

		// defaultView在浏览器中返回关联document的window对象，如果没有则返回null
		document = doc;
		docElem = doc.documentElement;
		parent = doc.defaultView;

		//如果 iframe文档已经指定了document并且如果iframe重载 
	    //当访问document变量时,IE将抛出"permission denied"错误 
	    //IE6-8不支持defaultView所以parent变量为undefined 
	    //要在unload的时候重新setDocument()  
	    //这个判断说明当前代码在frame中 
		if ( parent && parent !== parent.top ) {
			//frame卸载时重新设置document 
			if ( parent.addEventListener ) {
				//未传入参数document=window.document  
				parent.addEventListener( "unload", unloadHandler, false );
			} else if ( parent.attachEvent ) {
				parent.attachEvent( "onunload", unloadHandler );
			}
		}




	    /*---------------------------------------------------------------------- */
		// 判断当前document是不是HTML
		documentIsHTML = !isXML( doc );




	    /*---------------------------------------------------------------------- */
		//一些能力检查,添加到support中,包括attributes,id,tag,class四个选择方法支持情况 
		// IE8以下div.getAttribute("className")会得到class  
		support.attributes = assert(function( div ) {
			div.className = "i";
			return !div.getAttribute("className");
		});
		//判断getElementsByTagName是否只返回元素节点（某些浏览器使用该方法会返回元素节点和注释节点）
		support.getElementsByTagName = assert(function( div ) {
			div.appendChild( doc.createComment("") );
			return !div.getElementsByTagName("*").length;
		});

		// Support: IE<9
		support.getElementsByClassName = rnative.test( doc.getElementsByClassName );

		//Support: IE<10
		//判断当前浏览器通过方法返回的是否是单纯的ID为相应值得元素（某些浏览器使用可能会返回属性name为对应值得元素）
		 //检查是否getElementById会根据name返回元素 
		support.getById = assert(function( div ) {
	    //将assert中创建的这个div插入DOM树并将它的id设置为expando（一个Sizzle自己生成的以“sizzle”开头的很长的字符串）。目的是不希望DOM中用户有设置的相同的id
			docElem.appendChild( div ).id = expando;
			return !doc.getElementsByName || !doc.getElementsByName( expando ).length;
		});



	    /*---------------------------------------------------------------------- */
	    //根据支持情况来重写id,tag,class选择的方法
		//如果getElementById确实只会检查ID，而不是连NAME一起检查的话，初始化Expr的find.ID方法和filter.ID方法
		if ( support.getById ) {
			Expr.find["ID"] = function( id, context ) {
				if ( typeof context.getElementById !== "undefined" && documentIsHTML ) {
					var m = context.getElementById( id );
					return m && m.parentNode ? [ m ] : [];
				}
			};
			Expr.filter["ID"] = function( id ) {
				//将转义字符转回字符串
				var attrId = id.replace( runescape, funescape );
				return function( elem ) {
					return elem.getAttribute("id") === attrId;
				};
			};
		} else {
			//IE6,7的getElementById因为会根据name返回元素  
	        //所以是不能用原生getElementById方法获取元素的所以删除   
			delete Expr.find["ID"];

			Expr.filter["ID"] =  function( id ) {
				var attrId = id.replace( runescape, funescape );
				return function( elem ) {
					var node = typeof elem.getAttributeNode !== "undefined" && elem.getAttributeNode("id");
					return node && node.value === attrId;
				};
			};
		}

		//getElementsByTagName是否只返回Element元素 
		Expr.find["TAG"] = support.getElementsByTagName ?
			function( tag, context ) {
				if ( typeof context.getElementsByTagName !== "undefined" ) {
					return context.getElementsByTagName( tag );

				//DocumentFragment 节点不属于文档树，继承的 parentNode 属性总是 null。
	            //当请求把一个 DocumentFragment 节点插入文档树时，插入的不是 DocumentFragment 自身，而是它的所有子孙节点
	            //这使得 DocumentFragment 成了有用的占位符，暂时存放那些一次插入文档的节点。它还有利于实现文档的剪切、复制和粘贴操作
				} else if ( support.qsa ) {
					return context.querySelectorAll( tag );
				}
			} :

			function( tag, context ) {
				var elem,
					tmp = [],
					i = 0,
					//DocumentFragment节点的支持
					results = context.getElementsByTagName( tag );

				//在*下只要元素节点,所以要处理一下
				if ( tag === "*" ) {
					while ( (elem = results[i++]) ) {
						if ( elem.nodeType === 1 ) {
							tmp.push( elem );
						}
					}

					return tmp;
				}
				return results;
			};
		
		// Class
		Expr.find["CLASS"] = support.getElementsByClassName && function( className, context ) {
			if ( documentIsHTML ) {
				return context.getElementsByClassName( className );
			}
		};


		
	    /*---------------------------------------------------------------------- */
		// 检测浏览器是否支持高级的qSA方法(querySelectorAll)，并同时为support.qsa赋值
		rbuggyMatches = [];
		rbuggyQSA = [];

	    //如果支持qsa方法那么对select和input做出处理
	    //并把可以的方法的正则写进rbuggyQSA中
		if ( (support.qsa = rnative.test( doc.querySelectorAll )) ) {
			assert(function( div ) {
				
				docElem.appendChild( div ).innerHTML = "<a id='" + expando + "'></a>" +
					"<select id='" + expando + "-\f]' msallowcapture=''>" +
					"<option selected=''></option></select>";
	            //E[attr^=val]：该选择器定位具有属性attr且属性值以val开头的任何元素E
	            //E[attr$=val]：该选择器与E[attr^=val]正好相反，定位具有属性attr且属性值以val结尾的任何元素E：
	            //E[attr*=val]：该选择器与E[attr~=val]相似，但更进一步，定位具有属性attr且属性值任意位置包含val的元素E，val可以是一个完整的单词，也可以是一个单词中的一部分：
				if ( div.querySelectorAll("[msallowcapture^='']").length ) {
					rbuggyQSA.push( "[*^$]=" + whitespace + "*(?:''|\"\")" );
				}

				// 对于ie8来说他处理Boolean类型属性和"value"类型的不一样所以这里做了统一
				if ( !div.querySelectorAll("[selected]").length ) {
					rbuggyQSA.push( "\\[" + whitespace + "*(?:value|" + booleans + ")" );
				}

				//E[attr~=val]：该选择器定位具有属性attr且属性值为完整单词 val 的任何元素E：可用空格分开
				if ( !div.querySelectorAll( "[id~=" + expando + "-]" ).length ) {
					rbuggyQSA.push("~=");
				}

				//对于Webkit/Opera中checked应该返回的是selected的option元素节点
				if ( !div.querySelectorAll(":checked").length ) {
					rbuggyQSA.push(":checked");
				}

				if ( !div.querySelectorAll( "a#" + expando + "+*" ).length ) {
					rbuggyQSA.push(".#.+[+~]");
				}
			});

			assert(function( div ) {
				
				var input = doc.createElement("input");
				input.setAttribute( "type", "hidden" );
				div.appendChild( input ).setAttribute( "name", "D" );

				// Support: IE8
				if ( div.querySelectorAll("[name=d]").length ) {
					rbuggyQSA.push( "name" + whitespace + "*[*^$|!~]?=" );
				}

				//enabled表示已经启用的input即能输入的框，同理disabled就是不能输入的input框
				if ( !div.querySelectorAll(":enabled").length ) {
					rbuggyQSA.push( ":enabled", ":disabled" );
				}

				// Opera 10-11 does not throw on post-comma invalid pseudos
				div.querySelectorAll("*,:x");
				rbuggyQSA.push(",.*:");
			});
		}
	    //ie8+和各大浏览器基本支持matchesSelector方法，当要写兼容就是加前缀
		if ( (support.matchesSelector = rnative.test( (matches = docElem.matches ||
			docElem.webkitMatchesSelector ||
			docElem.mozMatchesSelector ||
			docElem.oMatchesSelector ||
			docElem.msMatchesSelector) )) ) {

			assert(function( div ) {
				// Check to see if it's possible to do matchesSelector
				// on a disconnected node (IE 9)
				support.disconnectedMatch = matches.call( div, "div" );

				//pseudos是伪类选择器用的正则
				matches.call( div, "[s!='']:x" );
				rbuggyMatches.push( "!=", pseudos );
			});
		}
	    //至此两个要用来匹配的正则已经导入数组了
		rbuggyQSA = rbuggyQSA.length && new RegExp( rbuggyQSA.join("|") );
		rbuggyMatches = rbuggyMatches.length && new RegExp( rbuggyMatches.join("|") );




		/*---------------------------------------------------------------------- */
		//compareDocumentPosition() 方法比较两个节点，并返回描述它们在文档中位置的整数
		//用法node.compareDocumentPosition(node)
		//支持ie8+和各大主流浏览器
		hasCompare = rnative.test( docElem.compareDocumentPosition );
	    // 当节点contains自己时,返回true,contains是js的原生方法
		contains = hasCompare || rnative.test( docElem.contains ) ?
			function( a, b ) {
				var adown = a.nodeType === 9 ? a.documentElement : a,
				    //bup是b节点的父节点  
					bup = b && b.parentNode;
				return a === bup || !!( bup && bup.nodeType === 1 && (
					adown.contains ?
					    //contains的是bup而是b是为了避免contains自己时返回true 
						adown.contains( bup ) :
						 //为什么要&16，请看compareDocumentPosition的返回值  
	                    /*  000000         0              元素一致 
	                        000001         1              节点在不同的文档（或者一个在文档之外） 
	                        000010         2              节点 B 在节点 A 之前 
	                        000100         4              节点 A 在节点 B 之前 
	                        001000         8              节点 B 包含节点 A 
	                        010000         16             节点 A 包含节点 B 
	                        100000         32             浏览器的私有使用 */  
						a.compareDocumentPosition && a.compareDocumentPosition( bup ) & 16
				));
			} :
			function( a, b ) {
				if ( b ) {
					while ( (b = b.parentNode) ) {
						if ( b === a ) {
							return true;
						}
					}
				}
				return false;
			};
		//至此包含的方法contains就设计好了，组合了js原生的contains方法和compareDocumentPosition方法
	   



		/*---------------------------------------------------------------------- */
		// 在这里重写顺序方法，用来知道谁在前谁在后
		sortOrder = hasCompare ?
		function( a, b ) {

			// Flag for duplicate removal
			if ( a === b ) {
				hasDuplicate = true;
				return 0;
			}

			// 如果只有一个元素拥有排序方法.  
	        // a节点有排序方法返回-1,b节点有排序方法返回1 
			var compare = !a.compareDocumentPosition - !b.compareDocumentPosition;
			if ( compare ) {
				return compare;
			}

			// 如果两个元素属于一个文档计算位置那时候就放回0 
			compare = ( a.ownerDocument || a ) === ( b.ownerDocument || b ) ?
				a.compareDocumentPosition( b ) :
				1;

			// 如果节点不再同一文档或者不支持分离排序并且a,b不再同一文档 
			if ( compare & 1 ||
				(!support.sortDetached && b.compareDocumentPosition( a ) === compare) ) {

				// a节点包含在当前document返回-1
				if ( a === doc || a.ownerDocument === preferredDoc && contains(preferredDoc, a) ) {
					return -1;
				}
				// b节点包含在当前doucment返回1 
				if ( b === doc || b.ownerDocument === preferredDoc && contains(preferredDoc, b) ) {
					return 1;
				}

				//保持原有顺序  
				return sortInput ?
					( indexOf( sortInput, a ) - indexOf( sortInput, b ) ) :
					0;
			}
			//a在b之前返回-1，否则返回1  
			return compare & 4 ? -1 : 1;
		} :
		function( a, b ) {
			// Exit early if the nodes are identical
			if ( a === b ) {
				hasDuplicate = true;
				return 0;
			}

			var cur,
				i = 0,
				aup = a.parentNode,
				bup = b.parentNode,
				ap = [ a ],
				bp = [ b ];

			 //没有父亲节点说明可能是document或者以不再DOM树中的节点 
			if ( !aup || !bup ) {
				//如果a是document说明a在前返回-1  
	            //如果b是document说明b在前返回1  
	            //否则如果a有父节点说明b是disconnected返回-1  
	            //否则b有父节点a是disconnected返回1  
	            //如果a、b都是disconnected，使用indexOf方法
				return a === doc ? -1 :
					b === doc ? 1 :
					aup ? -1 :
					bup ? 1 :
					sortInput ?
					( indexOf( sortInput, a ) - indexOf( sortInput, b ) ) :
					0;

			 // 父亲是一个，使用siblingCheck(a,b)方法快速检查  
			} else if ( aup === bup ) {
				return siblingCheck( a, b );
			}

			// 否则需要遍历整个祖先
			cur = a;
			while ( (cur = cur.parentNode) ) {
				//将a的所有的祖先入栈
				ap.unshift( cur );
			}
			cur = b;
			while ( (cur = cur.parentNode) ) {
				//将b的所有的祖先入栈 
				bp.unshift( cur );
			}

			 // 如果同层的祖先是一个
			while ( ap[i] === bp[i] ) {
				i++;
			}

			return i ?
				// 比较a,b的同级一个祖先之下的两个兄弟节点的位置  
				siblingCheck( ap[i], bp[i] ) :

				// Otherwise nodes in our document sort first
				ap[i] === preferredDoc ? -1 :
				bp[i] === preferredDoc ? 1 :
				0;
		};

		return doc;
};





